class ForLoop
{
    static void Main()
    {
        // The For Loop is similar to the while loop
        // in case of this, the initialization; condition and incrementation is 
        // written in one line of the code
        // (initialization; condition; incrementation)
           
        for(int i = 1; i <= 10; i++)
        {
            Console.Write(i + " ");
        }
        Console.ReadKey();
    }
}